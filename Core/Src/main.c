int16_t my_PID_Controller(void)
{

  VoutMeasured = HAL_ADCEx_InjectedGetValue(&hadc2, ADC_INJECTED_RANK_1);
  last_error = error;
  error = (uint16_t) VoutTarget - (uint16_t ) VoutMeasured;

  if (operationmode == 0) // start-up
  {
	  phase_cntr++;
	  pid_out = phase_cntr;
	  if (error <= 5)
		  operationmode = 1;
	  errorSum = 0;
  }

  if (operationmode == 1) // close loop
  {

//	  errorSum = errorSum + error * 2;
//	  errorRate = (error - last_error)/2;

	errorProp = (Kp * error) / 10;
	errorSum = errorSum + ((Ki * error) / 10);

	// check if the change is so much to prevent oscillations, see below "pid_out_diff"
	pid_out_last = pid_out;

	if (errorSum > SAT_LIMIT)
	{
	  errorSum = SAT_LIMIT;
	}
	if (errorSum < -(SAT_LIMIT))
	{
	  errorSum = -(SAT_LIMIT);
	}

	// my understanding
	//pid_out = Kp * error + errorSum * Ki + Kd * errorRate;

	// from FW
	/*
	seterr = (-Kp * error) / 200;
	Int_term_Buck = Int_term_Buck + ((-Ki * error) / 200);
	pid_out = seterr + Int_term_Buck;
	*/
	pid_out =  errorProp + errorSum;

	// check if the change is so much to prevent oscillations, see above "pid_out_last"
	pid_out_diff = pid_out - pid_out_last;

	// this supposedly prevent sudden change in duty/phase
//	    if (pid_out_diff > MAX_DUTY_INC)
//	    {
//	    	pid_out += MAX_DUTY_INC;
//	    } else if (pid_out_diff > MAX_DUTY_INC)
//	    {
//	    	pid_out -= MAX_DUTY_INC;
//	    }

  }

  if (operationmode == 2) // start up until vfeed
  {
	  if (error >= 5)
	  {
		  phase_cntr++;
		  pid_out = phase_cntr;
		  // do not use below
//		  if ( phase_cntr < debug_phase)
//		  {
//			  phase_cntr++;
//			  pid_out = phase_cntr;
//		  }
	  } else
		  while(1);
  }

  if (operationmode == 3) // debug = slowly increase duty/phase until a certain value
  {
	  if ( phase_cntr < debug_phase)
	  {
		  phase_cntr++;
		  pid_out = phase_cntr;
	  }
  }

  if (operationmode == 4) // debug = fix duty cycle/phase
  {
	  pid_out = debug_phase;
  }

  if (operationmode == 7) // start delay
  {
	  if (delay_cntr < 200000)
	  {
		  delay_cntr++;
	  } else
	  {
		  delay_cntr = 0;
		  operationmode++;
	  }
  }

  if (operationmode == 8) // one time slow going up
  {
	  if (repetition_cntr < SLOW_NUM_REPETITION)
	  {
		  repetition_cntr++;
	  } else
	  {
		  phase_cntr++;
		  if ( phase_cntr >= MAX_PHASE)
		  {
			  if (delay_cntr < 25000)
			  {
				  delay_cntr++;
				  pid_out = MAX_PHASE;
				  phase_cntr = MAX_PHASE;
			  } else
			  {
				  operationmode = 9;
				  repetition_cntr = 0;
			  }
		  } else
		  {
			  pid_out = phase_cntr;
		  }
		  repetition_cntr = 0;
	  }
  }

  if (operationmode == 9) // phase up/down
  {

	  if (repetition_cntr < NUM_REPETITION)
	  {
		  repetition_cntr++;
	  } else
	  {
		  if (direction == 0)
		  {
			  phase_cntr++;
			  if ( phase_cntr >= MAX_PHASE)
			  {
				  direction = 1;
				  //HAL_HRTIM_WaveformCountStop_IT(&hhrtim1, HRTIM_TIMERID_MASTER | HRTIM_TIMERID_TIMER_A | HRTIM_TIMERID_TIMER_B);
			  }
			  pid_out = phase_cntr;
		  }

		  if (direction == 1)
		  {
			  phase_cntr--;
			  if ( phase_cntr <= MIN_PHASE)
			  {
				  direction = 0;
			  }
			  pid_out = phase_cntr;
		  }

		  repetition_cntr = 0;
	  }
  }

  // limit the phase
  if (pid_out >= MAX_PHASE)
  {
    pid_out = MAX_PHASE;
  }

  if (pid_out < MIN_PHASE)
  {
    pid_out = MIN_PHASE;
  }

  // DAC debug
//  HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, VoutConversion);	// PA4
//  HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, pid_out+2048);		// PA5
//  HAL_DAC_SetValue(&hdac2, DAC_CHANNEL_1, DAC_ALIGN_12B_R, errorSum>>2);	// PA6

  return  pid_out;
}

